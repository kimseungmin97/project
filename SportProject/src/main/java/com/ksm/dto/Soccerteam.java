package com.ksm.dto;

public class Soccerteam {
private int qseq;
private String team;
private int win;
private int draw;
private int lose;
private int point;
private int losepoint;
private int points;
public int getQseq() {
	return qseq;
}
public void setQseq(int qseq) {
	this.qseq = qseq;
}
public String getTeam() {
	return team;
}
public void setTeam(String team) {
	this.team = team;
}
public int getWin() {
	return win;
}
public void setWin(int win) {
	this.win = win;
}
public int getDraw() {
	return draw;
}
public void setDraw(int draw) {
	this.draw = draw;
}
public int getLose() {
	return lose;
}
public void setLose(int lose) {
	this.lose = lose;
}
public int getPoint() {
	return point;
}
public void setPoint(int point) {
	this.point = point;
}
public int getLosepoint() {
	return losepoint;
}
public void setLosepoint(int losepoint) {
	this.losepoint = losepoint;
}
public int getPoints() {
	return points;
}
public void setPoints(int points) {
	this.points = points;
}
}
